import { Glob } from "bun";
import { homedir } from "node:os";
import { join, basename } from "node:path";
import { statSync } from "node:fs";
import type {
  Adapter,
  NormalizedMessage,
  NormalizedSession,
  ParseResult,
  SourceRef,
} from "./types.ts";
import { shellQuote } from "./shell.ts";

const DEFAULT_PROJECTS_DIR = join(homedir(), ".claude", "projects");

/** Discovery root. `TROVE_CC_ROOT` overrides the default `~/.claude/projects`;
 *  read per call (not at module load) so tests can point it at a fixture tree. */
function projectsDir(): string {
  return process.env.TROVE_CC_ROOT || DEFAULT_PROJECTS_DIR;
}

/** Extract the "meat" from one CC message.content: keep text (incl. code), drop
 *  thinking / tool_result bodies / images; record tool_use as a compact marker. */
function extractContent(content: unknown): { text: string; hasProse: boolean } {
  if (content == null) return { text: "", hasProse: false };
  if (typeof content === "string") {
    const s = content.trim();
    return { text: s, hasProse: s.length > 0 };
  }
  if (!Array.isArray(content)) return { text: "", hasProse: false };

  const prose: string[] = [];
  const toolNames: string[] = [];
  for (const block of content) {
    if (!block || typeof block !== "object") continue;
    const b = block as Record<string, unknown>;
    switch (b.type) {
      case "text":
        if (typeof b.text === "string" && b.text.trim()) prose.push(b.text.trim());
        break;
      case "tool_use":
        if (typeof b.name === "string") toolNames.push(b.name);
        break;
      // thinking, redacted_thinking, tool_result, image → dropped (bulk + noise)
    }
  }
  if (prose.length) return { text: prose.join("\n\n"), hasProse: true };
  if (toolNames.length) {
    const uniq = [...new Set(toolNames)];
    return { text: `[used: ${uniq.join(", ")}]`, hasProse: false };
  }
  return { text: "", hasProse: false };
}

// Harness-injected "user" turns that aren't the human talking (background-task results,
// system reminders, slash-command wrappers, local command output). Dropped at ingest.
const SYNTHETIC_PREFIXES = [
  "<task-notification>",
  "<system-reminder>",
  "<local-command-caveat>",
  "<local-command-stdout>",
  "<local-command-stderr>",
  "<command-name>",
  "<command-message>",
  "<command-args>",
  "<bash-input>",
  "<bash-stdout>",
  "<bash-stderr>",
  "<user-prompt-submit-hook>",
];
function isSyntheticUser(text: string): boolean {
  const t = text.trimStart();
  if (t.startsWith("Caveat: The messages below were generated by the user while running"))
    return true;
  return SYNTHETIC_PREFIXES.some((p) => t.startsWith(p));
}

export const claudeCodeAdapter: Adapter = {
  agentId: "claude-code",

  discoverLocations() {
    return [projectsDir()];
  },

  async enumerate(): Promise<SourceRef[]> {
    const root = projectsDir();
    const refs: SourceRef[] = [];
    const glob = new Glob("**/*.jsonl");
    try {
      for await (const rel of glob.scan({ cwd: root, onlyFiles: true })) {
        // Skip subagent transcripts (`<session>/subagents/agent-*.jsonl`) and journals —
        // internal noise; their content echoes back into the parent session.
        const base = basename(rel);
        if (rel.includes("/subagents/") || base.startsWith("agent-") || base === "journal.jsonl")
          continue;
        const path = join(root, rel);
        let st;
        try {
          st = statSync(path);
        } catch {
          continue;
        }
        refs.push({
          agent: this.agentId,
          medium: "file",
          path,
          sizeBytes: st.size,
          mtimeMs: Math.floor(st.mtimeMs),
        });
      }
    } catch {
      // projects dir absent → no sessions
    }
    return refs;
  },

  async parse(ref: SourceRef): Promise<ParseResult | null> {
    const bytes = await Bun.file(ref.path).bytes();
    const hasher = new Bun.CryptoHasher("sha256");
    hasher.update(bytes);
    const contentHash = hasher.digest("hex");
    const text = new TextDecoder().decode(bytes);

    const messages: NormalizedMessage[] = [];
    let seq = 0;
    let sessionId: string | null = null;
    let projectPath: string | null = null;
    let model: string | null = null;
    let title: string | null = null;
    let minTs = Infinity;
    let maxTs = -Infinity;

    for (const line of text.split("\n")) {
      if (!line.trim()) continue;
      let obj: any;
      try {
        obj = JSON.parse(line);
      } catch {
        continue; // fail soft on a bad line
      }
      const t = obj.type;
      if (!title) {
        if (t === "summary" && typeof obj.summary === "string") title = obj.summary;
        else if (typeof obj.customTitle === "string") title = obj.customTitle;
      }
      if (t !== "user" && t !== "assistant") continue;
      const m = obj.message;
      if (!m) continue;

      if (!sessionId && typeof obj.sessionId === "string") sessionId = obj.sessionId;
      if (!projectPath && typeof obj.cwd === "string") projectPath = obj.cwd;
      if (typeof m.model === "string") model = m.model;

      const extracted = extractContent(m.content);
      if (!extracted.text) continue; // pure thinking / tool_result / empty → skip
      // Drop harness-injected pseudo-user turns (not the human talking).
      if (m.role !== "assistant" && isSyntheticUser(extracted.text)) continue;

      const role: NormalizedMessage["role"] =
        m.role === "assistant"
          ? extracted.hasProse
            ? "assistant"
            : "tool"
          : extracted.hasProse
            ? "user"
            : "tool";

      const ts = typeof obj.timestamp === "string" ? Date.parse(obj.timestamp) : NaN;
      if (!Number.isNaN(ts)) {
        minTs = Math.min(minTs, ts);
        maxTs = Math.max(maxTs, ts);
      }

      messages.push({
        uid: typeof obj.uuid === "string" ? obj.uuid : null,
        seq: seq++,
        role,
        parentUid: typeof obj.parentUuid === "string" ? obj.parentUuid : null,
        timestamp: Number.isNaN(ts) ? null : ts,
        text: extracted.text,
      });
    }

    const st = statSync(ref.path);
    // Identity is the filename stem (unique per file). The in-content sessionId is shared
    // across resumes/subagents, so it must NOT be the key — keep it for later grouping.
    const nativeId = basename(ref.path).replace(/\.jsonl$/, "");
    const session: NormalizedSession = {
      nativeId,
      projectPath,
      createdAt: Number.isFinite(minTs) ? minTs : Math.floor(st.mtimeMs),
      updatedAt: Number.isFinite(maxTs) ? maxTs : Math.floor(st.mtimeMs),
      model,
      sourceTitle: title,
      kind: null,
      agentSpecific: {
        sourceFile: ref.path,
        projectDir: basename(join(ref.path, "..")),
        contentSessionId: sessionId,
      },
      messages,
    };
    return { session, contentHash, raw: bytes };
  },

  resolveProject(session: NormalizedSession): string | null {
    return session.projectPath ?? null;
  },

  buildResumeCommand(input): string | null {
    if (!input.nativeId) return null;
    const cd = input.projectPath ? `cd ${shellQuote(input.projectPath)} && ` : "";
    return `${cd}claude --resume ${input.nativeId}`;
  },
};
